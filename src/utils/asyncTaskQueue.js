/**
 * 异步任务队列类
 * 用于控制异步任务的并发执行数量，确保同时运行的任务不超过设定的最大值
 */
class AsyncTaskQueue {
  /**
   * 构造函数：初始化任务队列
   * @param {number} maxConcurrency - 最大并发数，默认为3（可同时执行3个任务）
   */
  constructor(maxConcurrency = 3) {
    // 步骤1：存储最大并发执行数量
    // 作用：限制同时运行的任务数量，防止资源过载
    this.maxConcurrency = maxConcurrency;

    // 步骤2：初始化任务队列数组，用于存储待执行的任务函数
    // 作用：按照 FIFO（先进先出）原则管理任务执行顺序
    this.queue = [];

    // 步骤3：初始化当前正在运行的任务计数器
    // 作用：跟踪当前并发执行的任务数量，用于判断是否可以启动新任务
    this.running = 0;
  }

  /**
   * 添加任务到队列
   * @param {Array<Function>} tasks - 要添加的任务函数数组，每个任务函数应返回 Promise
   */
  addTask(tasks) {
    // 步骤4：遍历所有要添加的任务
    // 作用：逐个处理传入的任务数组中的每个任务
    for (let i = 0; i < tasks.length; i++) {
      // 步骤5：将每个任务添加到队列末尾
      // 作用：使用 push() 方法将任务加入队列，保证任务按添加顺序排队
      this.queue.push(tasks[i]);

      // 步骤6：尝试执行任务（如果当前未达到并发上限，会立即执行）
      // 作用：每次添加任务后立即检查是否可以执行
      // 注意：run() 内部会检查并发限制，如果已达到上限，任务会留在队列中等待
      this.run();
    }
  }

  /**
   * 执行队列中的任务
   * 这是一个递归方法，会在任务完成后自动执行下一个任务
   */
  run() {
    // 步骤7：检查是否达到并发上限或队列为空
    // 条件1：this.running >= this.maxConcurrency - 当前运行的任务数已达到最大并发数
    // 条件2：this.queue.length === 0 - 队列中没有待执行的任务
    // 作用：如果满足任一条件，则停止执行，避免超出并发限制或执行空任务
    if (this.running >= this.maxConcurrency || this.queue.length === 0) {
      return;
    }

    // 步骤8：从队列头部取出一个任务（shift() 移除并返回数组第一个元素）
    // 作用：按照 FIFO 原则获取下一个要执行的任务，保证任务按添加顺序执行
    const task = this.queue.shift();

    // 步骤9：增加正在运行的任务计数
    // 作用：标记即将启动一个新任务，更新并发计数，防止超出并发限制
    this.running++;

    // 步骤10：执行任务函数（task 是一个返回 Promise 的函数）
    // 使用 finally() 确保无论任务成功还是失败，都会执行清理逻辑
    // 作用：异步执行任务，不阻塞其他任务的添加和执行
    task().finally(() => {
      // 步骤11：任务完成后（成功或失败），减少正在运行的任务计数
      // 作用：释放一个并发槽位，允许执行队列中的下一个任务
      this.running--;

      // 步骤12：递归调用 run()，尝试执行队列中的下一个任务
      // 作用：保证队列中的任务能够持续被执行
      // - 当一个任务完成时，立即检查是否有新任务可以执行
      // - 充分利用并发槽位，提高执行效率
      // - 形成自动化的任务执行循环
      this.run();
    });
  }
}
